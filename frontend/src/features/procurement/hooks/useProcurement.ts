export const useVendorTransactionHistory = (vendorId: number | null) => {
    return useQuery({
        queryKey: ['vendor-history', vendorId],
        queryFn: async () => {
            const [invoices, payments, returns] = await Promise.all([
                apiClient.get('/accounting/vendor-invoices/', { params: { vendor: vendorId, page_size: 9999 } }),
                apiClient.get('/accounting/payments/', { params: { vendor: vendorId, page_size: 9999 } }),
                apiClient.get('/procurement/purchase-returns/', { params: { vendor: vendorId, page_size: 9999 } }),
            ]);
            return {
                invoices: invoices.data.results || invoices.data || [],
                payments: payments.data.results || payments.data || [],
                returns: returns.data.results || returns.data || [],
            };
        },
        enabled: !!vendorId,
        staleTime: STALE_TIME,
    });
};

