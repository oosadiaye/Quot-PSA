/**
 * Fetch the processed DownPaymentRequest for a specific PO (if any).
 * Returns null when no processed DPR exists.
 * Used by the invoice verification form to show the "Apply Down Payment" section.
 */
export const useDownPaymentForPO = (poId: number | null | undefined) => {
    return useQuery({
        queryKey: ['down-payment-for-po', poId],
        queryFn: async () => {
            const { data } = await apiClient.get(
                `/procurement/down-payment-requests/?purchase_order=${poId}&status=Processed&page_size=1`,
            );
            const results = data?.results ?? data;
            return Array.isArray(results) && results.length > 0 ? results[0] : null;
        },
        enabled: !!poId,
        staleTime: STALE_TIME,
    });
};

/**
 * Apply (or adjust) a down payment deduction against an InvoiceMatching record.
 * POST /procurement/invoice-matching/{id}/apply_down_payment/
 * Body: { amount?: number }  — omit to apply the full available advance balance.
 */
export const useApplyDownPayment = () => {
    const queryClient = useQueryClient();
    return useMutation({
        mutationFn: async ({ matchingId, amount }: { matchingId: number; amount?: number }) => {
            const body: Record<string, any> = {};
            if (amount !== undefined) body.amount = amount;
            const { data } = await apiClient.post(
                `/procurement/invoice-matching/${matchingId}/apply_down_payment/`,
                body,
            );
            return data;
        },
        onSuccess: () => {
            queryClient.invalidateQueries({ queryKey: ['invoice-matchings'] });
            queryClient.invalidateQueries({ queryKey: ['down-payment-requests'] });
            queryClient.invalidateQueries({ queryKey: ['down-payment-for-po'] });
        },
    });
};
